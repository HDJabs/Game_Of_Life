    public static void update(){
        boolean[][] flipBoard = new boolean[width()][height()];
        for(int x=0; x<width(); x++){
            for(int y=0; y<height(); y++){
                flipBoard[x][y] = nodeAt(x, y).isAlive();
            }
        }

        for(int x=0; x<width(); x++){
            for(int y=0; y<height(); y++){
                Node currentNode = nodeAt(x, y);

                if(currentNode.getNumberOfAliveNeighbors()==3)
                    flipBoard[x][y]=true;
                
                if(currentNode.isAlive()){
                    if(currentNode.getNumberOfAliveNeighbors()<2)
                        flipBoard[x][y] = false;
                    if(currentNode.getNumberOfAliveNeighbors()>3)
                        flipBoard[x][y] = false;
                }
            }
        }


        for(int x=0; x<width(); x++){
            for(int y=0; y<height(); y++){
                nodeAt(x, y).setLife(flipBoard[x][y]);
            }
        }

    }

    public static void flip(int x, int y){
        if(x<0||x>width()||y<0||y>height())
            throw new GameOfLifeException("out of bounds, bucko");
        nodeAt(x, y).setLife(!nodeAt(x, y).isAlive());
    }

    public static void fillBoard(int[][] array){
        nodeArray.setBoardHeight(array[0].length);
        nodeArray.setBoardWidth(array[1].length);
        for(int x=0; x<array[1].length;x++){
            for(int y=0; y<array[0].length;y++){
                nodeAt(x, y).setLife(array[x][y]==1? true : false);
            }
        }
        System.out.println("end of fillboard");
    }

    //SHORTCUTS TO TIGHTEN UP CODE
    public static Node nodeAt(int number1, int number2){
        return nodeArray.getNodeAt(number1, number2);
    }
    public static int width(){
        return nodeArray.getBoardWidth();
    }
    public static int height(){
        return nodeArray.getBoardHeight();
    }

    public static void printAlive(){
        
        for(int x=0; x<width(); x++){
            String s = "";
            for(int y=0; y<height(); y++){
                int alive = nodeAt(y, x).getNumberOfAliveNeighbors();
                if(alive!=0)
                    s += alive + ", ";
                else
                    s += " , ";
            }
            System.out.println(s);
        }
    }

    public static void randomBoard(){
        Random random = new Random();
        for(int x=0; x<width(); x++)
            for(int y=0; y<height(); y++)
                nodeAt(x, y).setLife(random.nextBoolean());
    }
